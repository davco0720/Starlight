    public void MostrarTabla() {
        try {
            String query = "SELECT Users.Username AS Usuario, Scores.Puntaje, Scores.Fecha, Scores.Tiempo_Restante\n"
                    + "FROM Scores\n"
                    + "INNER JOIN Users\n"
                    + "ON Scores.ID_User = Users.ID_User\n"
                    + "ORDER BY Scores.Puntaje DESC;";
            Statement stan = (Statement) conexion.ConexionBD().createStatement();
            ResultSet resultSet = stan.executeQuery(query);
            DefaultTableModel tableModel = new DefaultTableModel();
            ResultSetMetaData metaData = (ResultSetMetaData) resultSet.getMetaData();
            int columnCount = metaData.getColumnCount();
            for (int i = 1; i <= columnCount; i++) {
                tableModel.addColumn(metaData.getColumnName(i));
            }
            while (resultSet.next()) {
                Object[] rowData = new Object[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    rowData[i - 1] = resultSet.getObject(i);
                }
                tableModel.addRow(rowData);
            }
            tabla.setModel(tableModel);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos: " + e.getMessage());
        }
    }

